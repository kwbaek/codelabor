package org.codelabor.example.dtos;

/**
 * @author SangJae Shin
 * 
 */
public class UserDTO {

	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	protected String userId = null;
	protected String userName = null;
	protected String userPassword = null;
	/**
	 * Constructs a <code>String</code> with all attributes
	 * in name = value format.
	 *
	 * @return a <code>String</code> representation 
	 * of this object.
	 */
	public String toString()
	{
	    final String TAB = "    ";
	
	    StringBuilder retValue = new StringBuilder();
	    
	    retValue.append("UserDTO ( ")
	        .append(super.toString()).append(TAB)
	        .append("userId = ").append(this.userId).append(TAB)
	        .append("userName = ").append(this.userName).append(TAB)
	        .append("userPassword = ").append(this.userPassword).append(TAB)
	        .append(" )");
	    
	    return retValue.toString();
	}
	

}
